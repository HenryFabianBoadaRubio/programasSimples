#escriba un programa que calcule el promedio de 4 notas ingresadas por el usuario:

n1=float(input("ingrese por favor el valor de la nota 1 \n"))
n2=float(input("ingrese por favor el valor de la nota 2 \n"))
n3=float(input("ingrese por favor el valor de la nota 3 \n"))
n4=float(input("ingrese por favor el valor de la nota 4 \n"))
promedio=(n1+n2+n3+n4)/4

print(f"el promedio general de las notas es = {promedio}")